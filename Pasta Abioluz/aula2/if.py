# Media >= 7 aprovado
# Media < 7 Reprovado

media = 7

if media >= 7:
    #identação
    #este bloco irá executar se a condição
    # media >= 7 for True (verdadeira)
    print("Aprovado")

else:
    # irá executar se media >= 7 for False (falso)
    print("Reprovado")

