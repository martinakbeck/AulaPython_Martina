def media (n1, n2, n3):
    calculo = (n1 + n2 + n3)/3
    return calculo

def media_lista (lista):
    calculo = sum(lista)/len(lista)
    return calculo

def soma (n1, n2):
    total = n1 + n2
    return total

def diminuicao (n1, n2):
    total = n1 - n2
    return total

def divisao (n1, n2):
    total = n1 / n2
    return total

def multiplicacao (n1, n2):
    total = n1 * n2
    return total
     
